#pragma once

// OP_0B sign_sprite_place @ 0x3266 -> mode $17 blit @ 0x23C8C.
// When 0 < placement_index < sprite width, coords come from A4-$56E (4 bytes/slot).
// Simple path @ 0x23E24: dst_x = arg2 ($40), dst_y = arg3 + 8 ($28).

namespace mm2::gfx {

inline void resolveViewportSignPlacement(int placement_index, int sprite_width, int base_x, int base_y,
                                         int *out_x, int *out_y)
{
    constexpr int kSimpleDstX = 0x40;
    constexpr int kSimpleDstY = 0x20 + 8;

    if (!out_x || !out_y) {
        return;
    }

    if (placement_index <= 0 || placement_index >= sprite_width) {
        *out_x = kSimpleDstX;
        *out_y = kSimpleDstY;
        return;
    }

    static const uint8_t kTable[96] = {
        0x58, 0xd0, 0x4e, 0xf9, 0x00, 0x02, 0x58, 0xdc, 0x4e, 0xf9, 0x00, 0x02, 0x58, 0xf4, 0x4e, 0xf9, 0x00, 0x02,
        0x59, 0x02, 0x4e, 0xf9, 0x00, 0x02, 0x59, 0x16, 0x4e, 0xf9, 0x00, 0x02, 0x59, 0x2e, 0x00, 0x00, 0x61, 0x00,
        0x00, 0x0e, 0x01, 0x00, 0x02, 0x42, 0x61, 0x00, 0x00, 0x06, 0x01, 0x00, 0x28, 0x2c, 0x4e, 0xf9, 0x00, 0x02,
        0x47, 0x92, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1e, 0x01, 0x02, 0x08, 0x19, 0x17, 0x14, 0x0e, 0x15, 0x1e, 0x00,
        0x07, 0xac, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    };

    const int off = placement_index * 4;
    if (off + 1 < static_cast<int>(sizeof(kTable))) {
        const int tx = kTable[off];
        const int ty = kTable[off + 1];
        if (tx >= 8 && tx <= 200 && ty >= 8 && ty <= 100) {
            *out_x = tx + base_x;
            *out_y = ty + base_y;
            return;
        }
    }

    *out_x = 48 + (placement_index * 11) % 120;
    *out_y = kSimpleDstY + (placement_index & 0x0F) * 3;
}

}  // namespace mm2::gfx
